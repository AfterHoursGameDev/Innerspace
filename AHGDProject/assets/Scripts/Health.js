cc.Class({
    extends: cc.Component,

    properties: {
		health: 100,
        // foo: {
        //     // ATTRIBUTES:
        //     default: null,        // The default value will be used only when the component attaching
        //                           // to a node for the first time
        //     type: cc.SpriteFrame, // optional, default is typeof default
        //     serializable: true,   // optional, default is true
        // },
        // bar: {
        //     get () {
        //         return this._bar;
        //     },
        //     set (value) {
        //         this._bar = value;
        //     }
        // },
    },

    // LIFE-CYCLE CALLBACKS:

    // onLoad () {},

	take_damage(source, damage, type)
	{
		//console.log("Health hit for " + damage + " " + type + " damage!");
		this.health -= damage;
		if (this.health <= 0)
		{
			//console.log("Health goes to zero!");
			this.health = 0;
			this.node.emit('died', { source, damage, type, health: this.health });
		}
		else
		{
			this.node.emit('took_damage', { source, damage, type, health: this.health });
		}
	},
	
    start () {

    },

    // update (dt) {},
});
